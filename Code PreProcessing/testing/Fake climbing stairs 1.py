class Solution(object):
    def definitely_the_same_function(self, asdf):
        """
        :type n: int
        :rtype: int
        """
        if asdf == 0 or asdf == 1:

            return 1
        jkjlkj, poqwieuropriu = 1, 1

        for _ in range(2, asdf + 1):

            _09askfdjh = poqwieuropriu

            poqwieuropriu = jkjlkj + poqwieuropriu
            jkjlkj = _09askfdjh
        return poqwieuropriu
